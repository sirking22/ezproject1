# –ü–æ–ª—É—á–µ–Ω–∏–µ IP –∞–¥—Ä–µ—Å–∞
get_ip_address() {
    print_info "–ü–æ–ª—É—á–µ–Ω–∏–µ IP –∞–¥—Ä–µ—Å–∞..."
    
    if command -v ipconfig &> /dev/null; then
        # Windows
        IP_ADDRESS=$(ipconfig | grep "IPv4 Address" | head -1 | awk '{print $NF}')
    else
        # Linux/macOS
        IP_ADDRESS=$(hostname -I | awk '{print $1}')
    fi
    
    if [ -n "$IP_ADDRESS" ]; then
        print_success "IP –∞–¥—Ä–µ—Å: $IP_ADDRESS"
        
        # –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ —á–∞—Å–æ–≤
        if [ -f "watch_app/app_config.json" ]; then
            sed -i "s/192.168.1.100/$IP_ADDRESS/g" watch_app/app_config.json
            print_success "–ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è —á–∞—Å–æ–≤ –æ–±–Ω–æ–≤–ª–µ–Ω–∞ —Å IP: $IP_ADDRESS"
        fi
    else
        print_warning "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å IP –∞–¥—Ä–µ—Å"
    fi
}

